-- 01 TP0001 - OBRA - DESCRICAO DA OBRA, SEU CONTEUDO INTELECTUAL E IMATERIAL
OBRA_COD INT NOT NULL AUTOINCREMENT, 
TITULO CHAR(60) NOT NULL, 
SUBTITULO CHAR(60), 
TITULO_ORIGINAL CHAR(60), 
IDIOMA_ORIGINAL_COD INT NULL, --FOREIGN KEY
GENERO_COD INT NULL, --FOREIGN KEY
SUMARIO VARCHAR(2048), 
PRIMARY KEY IS (OBRA_COD)


-- 02 TP002 - EXEMPLAR - DESCRICAO DO EXEMPLAR, CONTEUDO FISICO, OBJETO
EXEMPLAR_COD INT NOT NULL AUTOINCREMENT, 
OBRA_COD INT NOT NULL, --FOREIGN KEY
EDITORA_COD INT NULL, --FOREIGN KEY
DATA_EDICAO CHAR(8) NULL, 
EDICAO_NO INT NULL, 
EDICAO_ANO CHAR(4) NULL, 
EDICAO_CIDADE CHAR(30) NULL, 
QTD_PAGINAS INT NULL, 
PRECO_MOEDA CHAR(3) NULL,
PRECO_VALOR DECIMAL(5,2) NULL, 
CODIGO_BARRAS CHAR(13) NULL, 
ISBN CHAR(13) NULL, 
ISSN CHAR(13) NULL, 
CDD CHAR(10) NULL, 
COLECAO CHAR(30) NULL, 
COLECAO_NO CHAR(10) NULL, 
FORMATO_COD INT NULL, --FOREIGN KEY
PRIMARY KEY IS (ELEMENTO_COD, OBRA_COD) 


-- 03 TP003 PESSOA 
PESSOA_COD INT NOT NULL AUTOINCREMENT, 
NOME CHAR(60) NOT NULL, 
SOBRENOME CHAR(60) NULL, 
ANO_NASC CHAR(4) NULL, 
BIO VARCHAR(2048), 
PRIMARY KEY IS PESSOA_COD 

-- 04 TP004 EDITORA
EDITORA_COD INT NOT NULL AUTOINCREMENT, 
NOME CHAR(60) NOT NULL, 
ENDERECO NULL, --FOREIGN KEY
CIDADE CHAR(30) NULL, 
SITE CHAR(60) NULL, 
EMAIL CHAR(60) NULL, 
PRIMARY KEY IS (EDITORA_COD) 

-- 05 TT005 IDIOMA 
IDIOMA_COD INT NOT NULL AUTOINCREMENT, 
IDIOMA CHAR(25) NOT NULL, 
ISO639 CHAR(5) NOT NULL ,
PRIMARY KEY IS (IDIOMA_COD) 

-- 06 TT006-FORMATO 
FORMATO_COD INT NOT NULL AUTOINCREMENT, 
FORMATO CHAR(25) NOT NULL, 
PRIMARY KEY IS(FORMATO_COD) 
-- DOMINIO: Hardcover, Softcover, partitura, 
-- revista, jornal, fasciculo

-- 07 TP007 TAG 
OBRA_COD NOT NULL, --FOREIGN KEY
TAG_COD INT NOT NULL, 
TAG CHAR(30), 
PRIMARY KEY IS (OBRA_COD, TAG_COD)

-- 08 TT008 PAPEL 
PAPEL_COD INT NOT NULL AUTOINCREMENT, 
PAPEL CHAR(30), 
PRIMARY KEY IS (PAPEL_COD) 
--Dominio: Tradutor, ilustrador, apresentador, 
-- organizador, editor, prefacio, etc

-- 09 TT009 GENERO
GENERO_COD INT NOT NULL AUTOINCREMENT, 
GENERO CHAR(30), 
PRIMARY KEY IS (GENERO_COD) 

-- 10 TT010 PAIS 
PAIS_COD INT NOT NULL AUTOINCREMENT, 
PAIS CHAR(30) NOT NULL, 
PAIS_SG CHAR(2) NULL, 
PRIMARY KEY IS (PAIS_COD) 

-- 11 TA011 AUTORES 
OBRA_COD NOT NULL, --FOREIGN KEY
AUTOR_COD NOT NULL, --FOREIGN KEY
PRIMARY KEY IS(OBRA_COD, AUTOR_COD) 

-- 12 TA012 PRODUTORES 
EXEMPLAR_COD NOT NULL, --FOREIGN KEY
PESSOA_COD NOT NULL, --FOREIGN KEY
PAPEL_COD NOT NULL, --FOREIGN KEY
PRIMARY KEY IS(EXEMPLAR_COD, 
	PESSOA_COD, PAPEL_COD) 

-- 13 TP013 IDX CATLG
OBRA_COD NOT NULL, --FOREIGN KEY
IDX_CATLG_COD INT NOT NULL, 
IDX_CATLG CHAR(30) NOT NULL, 
